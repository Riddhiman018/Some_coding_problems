    if num1<10 or num2<10:
        return ((num1)*(num2))
    string1 = str(num1)
    string2 = str(num2)
    a = int(string1[:len(string1)//2])
    b = int(string1[len(string1)//2:])
    c = int(string2[:len(string2)//2])
    d = int(string2[len(string2)//2:])
    return ((10**(len(string1))*KARAT(a,c))+KARAT(b,d) + (10**(len(string1)//2))*(KARAT(a,d) + KARAT(b,c)))

